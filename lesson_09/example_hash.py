"""
НАВЧАЛЬНИЙ ПРИКЛАД: Що таке хеш і як він працює в Python

Хеш (hash) - це функція, яка перетворює дані будь-якого розміру 
на фіксований розмір (хеш-значення). Це як "відбиток пальця" для даних.

Основні властивості:
1. Детермінованість: однакові дані = однаковий хеш
2. Швидкість: обчислення O(1) в середньому
3. Використовується в dict, set для швидкого пошуку
"""

print("=" * 60)
print("1. ВБУДОВАНА ФУНКЦІЯ hash() В PYTHON")
print("=" * 60)

# Базове використання hash()
text1 = "Hello"
text2 = "World"
text3 = "Hello"  # Те саме, що text1

print(f"hash('{text1}') = {hash(text1)}")
print(f"hash('{text2}') = {hash(text2)}")
print(f"hash('{text3}') = {hash(text3)}  # Однаковий з text1!")
print()

# Хеш чисел
print(f"hash(42) = {hash(42)}")
print(f"hash(3.14) = {hash(3.14)}")
print(f"hash((1, 2, 3)) = {hash((1, 2, 3))}")  # Кортежі можна хешувати
print(f"hash([1, 2, 3]) = ...")  # Списки НЕ можна хешувати (незмінні)
# print(hash([1, 2, 3]))  # Це викличе помилку TypeError
print()

